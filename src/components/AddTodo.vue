<template>
    <div>
        <form @submit="addTodo">
            <input type="text" v-model="title" name="title" placeholder="Add todo">
            <input type="submit" class="btn"  value="Submit">
        </form>
    </div>
</template>
<script>
// import { v4 as uuidv4 } from 'uuid';
export default {
    name:'AddTodos',
    data(){
        return{
            title:''
        }
    },
    methods:{
        addTodo(e){
            e.preventDefault();
            
            const newTodo={
                title:this.title,
                complete:false
            }
            // send to parent
            this.$emit('add-todo',newTodo);
        }
    }
}
</script>
<style scoped>
form {
    display: flex;
  }
  input[type="text"] {
    flex: 10;
    padding: 5px;
  }
  input[type="submit"] {
    flex: 2;
  }
</style>